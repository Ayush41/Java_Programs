public class Arrays{
    public static void main(String[] args){
        System.out.println("Working...");
        //Loops & Arrays
        for(int i =0;i<10;i++){
            System.out.println("i");
        }
        //Outer loop
        for(int i =1;i<=2;i++){
            System.out.println("Outer :" + i);

        for(int j=1;j<=3;j++){
            System.out.println("Inner :" + j);
        }
        }


    }
}